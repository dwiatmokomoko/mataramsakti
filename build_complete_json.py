def convert_to_json_format(data_dict):
    """Convert tuple format to JSON format"""
    result = {}
    result["comment"] = data_dict["comment"]
    
    for price, entries in data_dict.items():
        if price == "comment":
            continue
            
        result[price] = []
        for entry in entries:
            dp, ang11, ang17, ang23, ang29, ang35 = entry
            result[price].append({
                "dp": dp,
                "installments": {
                    "11": ang11,
                    "17": ang17,
                    "23": ang23,
                    "29": ang29,
                    "35": ang35
                }
            })
    
    return result

